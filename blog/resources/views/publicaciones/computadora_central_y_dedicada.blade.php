<p>Computadora central</p>
<p>Una computadora central o mainframe se caracteriza por ser una computadora grande, habitualmente era usada por empresas grandes con procesos centralizados. Estas computadoras tienen una gran capacidad de procesamiento y almacenamiento de información, y cuentan con múltiples terminales y por eso, las personas que usan una, debido a su gran tamaño, necesitan ser manipuladas por un equipo técnico altamente cualificado.
La capacidad de una computadora central se define tanto por la velocidad de su CPU como por su gran memoria interna, su alta y gran capacidad de almacenamiento externo, sus resultados en los dispositivos E/S rápidos y considerables, la alta calidad de su ingeniería interna que tiene como consecuencia una alta fiabilidad y soporte técnico costoso, pero de alta calidad. Una computadora central puede funcionar durante años sin problemas ni interrupciones y las reparaciones de este pueden ser realizadas mientras está funcionando.
</p>
<p>Computadora dedicada</p>
<p>Es un equipo informático físico que destina todos sus recursos a proporcionar información y atender las peticiones de otro ordenador (cliente) que ha contratado sus servicios. Por tanto, a diferencia de un servidor compartido, el dedicado trabaja en exclusiva para un solo cliente. Existe un amplio abanico de servidores dedicados entre los que poder elegir para el alojamiento en Internet.
Algunas de sus características son las siguientes:
<ul>
    <li>Se localiza, por regla general, en un centro de datos, lugar en el que se encuentran todos los recursos que una entidad necesita para procesar su información.</li>
    <li>Proporciona más seguridad y mayor velocidad, ya que los recursos de este gran ordenador central no se comparten con otros usuarios.</li>
    <li>Está destinado a alojar sitios web con un elevado tráfico, siendo recomendable por tanto que las que contratan estos servicios reciban un gran número de visitas diarias.</li>
    <li>Posee una alta cuota mensual, superando, incluso, los 100 euros al mes, ya que, entre otras cosas, es un solo cliente el que hace frente a los gastos de mantenimiento.</li>
    <li>Cuenta, en la mayoría de los casos, con un servicio de atención disponible las 24 horas del día y los 365 días del año.</li>
</ul>
</p>
